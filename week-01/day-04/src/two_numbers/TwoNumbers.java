package two_numbers;

public class TwoNumbers {
    public static void main(String[] args) {
        int a = 22, b = 13;

        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / (float)b);
        System.out.println(a / b);
        System.out.println(a % b);
    }
}
